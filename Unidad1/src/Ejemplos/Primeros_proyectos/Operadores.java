package Ejemplos.Primeros_proyectos;

public class Operadores {

	public static void main(String[] args) {
		
		int X;
		int Y;
		double N;
		
		X=2+2*6;
		Y=(34+4/2)+5;
		N=X^3;
				//X+1=X++ quiere decir con el ++ que le sumes 1
		
		System.out.println("Resultado de la operación X: " + X);

		System.out.println("Resultado de la operación Y: " + Y);
		
		System.out.println("Resultado de la operación N: " + N);
	}

}
